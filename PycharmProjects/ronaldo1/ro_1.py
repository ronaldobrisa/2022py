print(5 + 2, end=' ')#operador aritmético adição
print(5-2)#operador aritmético subtração
print(5*2, end=' ')#operador aritmético multiplicação
print(5/2)#operador aritmético divisão
print(5**2, end=' ')#operador aritmético potenciação
print(5%2)#operador aritmético resto da divisão
print(5//2, end=' ')#operador aritmético inteiro da divisão

